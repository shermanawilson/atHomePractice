public class Singles {
    String name;
    int age;
    float weight;

    public Singles(String n, int a, float w) {
        this.name = n;
        this.age = a;
        this.weight = w;
    }

    public Singles() {
        this.name = "They haven't given me a name yet :-(";
        this.age = 0;
        this.weight = 100;
    }


}
